//Fungsi Operasi Exercise 1
public class InputDefined
{
    public static void kalkulasi(int g, int m)
    {
      System.out.println("Hasil dari operasi penjumlahan antara" + g + "dan" + m + "adalah" + (g+m));
      System.out.println("Hasil dari operasi pengurangan antara" + g + "dan" + m + "adalah" + (g-m));
      System.out.println("Hasil dari operasi perkalian antara" + g + "dan" + m + "adalah" + (g*m));
      System.out.println("Hasil dari operasi pembagian antara" + g + "dan" + m + "adalah" + (g/m));
    }
}
